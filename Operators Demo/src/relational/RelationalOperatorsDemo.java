package relational;

public class RelationalOperatorsDemo {
	
	public static void main(String[] args) {
		
		int a=10;
		int b=35;
		int c=78;
		
		System.out.println("a == b :" + (a == b)); 
        System.out.println("a < b :" + (a < b)); 
        System.out.println("a <= c :" + (a <= c)); 
        System.out.println("a > b :" + (a > b)); 
        System.out.println("c >= b :" + (c >= b)); 
        System.out.println("a != b :" + (a != b));
		
	}

}
